import numpy as np


# Сгенерируем массив 5x6 со случайными числами от 0 до 99
array = np.random.randint(0, 100, size=(5, 6))
print(f"Массив:\n{array}\n")

# Получение атрибутов массива
print(f"Число измерений:\n{array.ndim}\n")
print(f"Размеры массива:\n{array.shape}\n")
print(f"Количество элементов:\n{array.size}\n")
print(f"Тип данных элементов:\n{array.dtype}\n")

# Выборка элемента по индексу и срезы
print(f"Элемент с индексом [2, 3]:\n{array[2, 3]}\n")
print(f"Срез первых 3 строк:\n{array[:3]}\n")
print(f"Срез 2-го и 3-го столбца:\n{array[:, 1:3]}\n")

# Математические и статистические функции
print(f"Сумма элементов:\n{np.sum(array)}\n")
print(f"Среднее значение:\n{np.mean(array)}\n")
print(f"Медиана:\n{np.median(array)}\n")
print(f"Отклонение от среднего:\n{np.std(array)}\n")
print(f"Максимальное значение:\n{np.max(array)}\n")
print(f"Минимальное значение:\n{np.min(array)}")
